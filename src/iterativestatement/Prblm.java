package iterativestatement;

public class Prblm {

	 public static void printPattern(int n) {
		 
	        for (int i = n; i > 0; i--) {
	            for (int j = 1; j < 0; j++) {
	                System.out.print(j + " ");
	            }
	            System.out.println();
	        }
	 }
}

	   
	        
	       
	    
	
 


